# In Python, type is a built-in function, not an operator.
type(5)           # <class 'int'>
type("hello")     # <class 'str'>
type([1, 2, 3])   # <class 'list'>

x = 10
type(x)           # <class 'int'>

y = 10.21
type(y)           # <class 'float'>

z = "Krutarth"
type(z)           # <class 'str'>

print(type(x))
print(type(y))
print(type(z))